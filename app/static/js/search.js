const searchInput = document.getElementById("search-input");
const countries = document.getElementById("suggestions-list");

searchInput.addEventListener("input", function() {
  const query = searchInput.value.toLowerCase();
  const filteredData = countryList.filter(item => item.toLowerCase().startsWith(query));
  displaySuggestions(filteredData);
});

function displaySuggestions(results) {
    countries.innerHTML = "";
    results.forEach(result => {
      const li = document.createElement("li");
      li.textContent = result;
      li.addEventListener("click", function() {
        searchInput.value = result;
        countries.innerHTML = "";
        window.location.href = "country/" + result;
      });
      countries.appendChild(li);
    });
    if (results.length === 0) {
        countries.innerHTML = "";
    }
  }